### Name: uniquegenelist
### Title: Eliminate Duplicate Names from the Gene List
### Aliases: uniquegenelist
### Keywords: array

### ** Examples

genelist <- c("A","A","B","B","C","C","D","D")
uniquegenelist(genelist,ndups=2)
genelist <- c("A","B","A","B","C","D","C","D")
uniquegenelist(genelist,ndups=2,spacing=2)



